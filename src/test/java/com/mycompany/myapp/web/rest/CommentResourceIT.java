    @Test
    @Transactional
    void createComment() throws Exception {
        int databaseSizeBeforeCreate = commentRepository.findAll().size();
        // Create the Comment
        CommentDTO commentDTO = commentMapper.toDto(comment);
        restCommentMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(commentDTO)))
            .andExpect(status().isCreated());

        // Validate the Comment in the database
        List<Comment> commentList = commentRepository.findAll();
        assertThat(commentList).hasSize(databaseSizeBeforeCreate + 1);
        Comment testComment = commentList.get(commentList.size() - 1);
        assertThat(testComment.getDepth()).isEqualTo(DEFAULT_DEPTH);
        assertThat(testComment.getComment()).isEqualTo(DEFAULT_COMMENT);
        assertThat(testComment.getReadCnt()).isEqualTo(DEFAULT_READ_CNT);
        assertThat(testComment.getGoodCnt()).isEqualTo(DEFAULT_GOOD_CNT);
        assertThat(testComment.getBadCnt()).isEqualTo(DEFAULT_BAD_CNT);
        assertThat(testComment.getCreatedAt()).isEqualTo(DEFAULT_CREATED_AT);
        assertThat(testComment.getCreatedBy()).isEqualTo(DEFAULT_CREATED_BY);
        assertThat(testComment.getModifiedAt()).isEqualTo(DEFAULT_MODIFIED_AT);
        assertThat(testComment.getModifiedBy()).isEqualTo(DEFAULT_MODIFIED_BY);
    }

